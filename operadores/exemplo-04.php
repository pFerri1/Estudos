<?php

$a = 30;
$b = 55;

var_dump($a > $b);

echo "<br>";

var_dump($a < $b);

echo "<br>";
//se for valores iguais retorna true
var_dump($a==$b);

echo "<br>";
//se for identicos retorna o valor true
var_dump($a===$b);

echo "<br>";
//se for valores diferentes retorna true
var_dump($a != $b);

echo "<br>";
//se for diferente tanto tipo de dados quanto valores retorna true
var_dump($a !== $b);

?>